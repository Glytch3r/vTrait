vTrait = vTrait or {}

--[[ 
function vTrait.getHungerLoss(pl)
    local min = SandboxVars.vTrait.HungerLossMin or 5
    local max = SandboxVars.vTrait.HungerLossMax or 15
    local hungerLoss = math.min(max, math.max(min, (ZombRand(1, 101) / 100)))
    return hungerLoss
end
function vTrait.getThirstLoss(pl)
    local min = SandboxVars.vTrait.ThirstLossMin or 5
    local max = SandboxVars.vTrait.ThirstLossMax or 15
    return math.min(max, math.max(min, (ZombRand(1, 101) / 100)))
end

function vTrait.dead(victim)
    if instanceof(victim, "IsoPlayer") and victim:HasTrait("V")   then
        vTrait.setSmoke(victim, false)
    end

    local attacker = victim:getAttackedBy()
    if attacker then
        if not attacker:HasTrait("V") or attacker ~= getPlayer() then
            return
        end
        vTrait.recover(attacker, 2)
    end
end
Events.OnZombieDead.Remove(vTrait.dead)
Events.OnZombieDead.Add(vTrait.dead)
Events.OnPlayerDeath.Remove(vTrait.dead)
Events.OnPlayerDeath.Add(vTrait.dead)

