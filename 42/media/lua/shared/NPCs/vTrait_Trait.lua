require "NPCs/MainCreationMethods"


MapSymbolDefinitions.getInstance():addTexture("V", "media/ui/LootableMaps/V_Symbol.png")

Events.OnGameBoot.Add(function()
  local cost = 30
	if getCore():getDebug() then cost = 1 end
  
	TraitFactory.addTrait("V", getText("UI_trait_V"), cost, getText("UI_trait_V_desc"), false)

	TraitFactory.setMutualExclusive("V", "FastHealer");
	TraitFactory.setMutualExclusive("V", "SlowHealer");
	TraitFactory.setMutualExclusive("V", "Hemophobic");

  TraitFactory.sortList()
  local traits = TraitFactory.getTraits()
  for i=0, traits:size()-1 do
    local trait = traits:get(i)
    BaseGameCharacterDetails.SetTraitDescription(trait)
  end

end)


