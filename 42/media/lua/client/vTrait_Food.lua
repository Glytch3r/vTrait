function vTrait.getHungerLoss(pl)
    local min = SandboxVars.vTrait.HungerLossMin or 5
    local max = SandboxVars.vTrait.HungerLossMax or 15
    local hungerLoss = math.min(max, math.max(min, (ZombRand(1, 101) / 100)))
    return hungerLoss
end
function vTrait.getThirstLoss(pl)
    local min = SandboxVars.vTrait.ThirstLossMin or 5
    local max = SandboxVars.vTrait.ThirstLossMax or 15
    return math.min(max, math.max(min, (ZombRand(1, 101) / 100)))
end



function vTrait.recover(pl, mult)
    if pl:HasTrait("V") then
        local shouldRecoverHunger = SandboxVars.vTrait.RecoverHunger or true
        local shouldRecoverThirst = SandboxVars.vTrait.RecoverThirst or true

        local stats = pl:getStats()
        if shouldRecoverHunger then
            local oldHunger = stats:getHunger()
            local hungerLoss = tonumber(vTrait.getHungerLoss(pl)) * mult
            local newHunger = oldHunger - hungerLoss
            stats:setHunger(newHunger)
        end

        if shouldRecoverThirst then
            local oldThirst = stats:getThirst()
            local thirstLoss = tonumber(vTrait.getThirstLoss(pl)) * mult
            local newThirst = oldThirst - thirstLoss
            stats:setThirst(newThirst)
        end

        local msg = "Hunger: " .. tostring(newHunger) .. " (-" .. tostring(hungerLoss) .. ")"
            .. "\nThirst: " .. tostring(newThirst) .. " (-" .. tostring(thirstLoss) .. ")"
        HaloTextHelper.addTextWithArrow(pl, msg, true, HaloTextHelper.getColorGreen())
    end
end ]]