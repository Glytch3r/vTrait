vTrait = vTrait or {}


function vTrait.doRoll(percent)
	if percent == 0 then return false end
	if percent == 100 then return true end
	return percent >= ZombRand(1, 101)
end

function vTrait.checkDist(pl, zed)
	local x, y = zed:getX(), zed:getY()
	local dist = pl:DistTo(x, y)
    return math.floor(dist)
end

function vTrait.getRandomSquareAround(zed, radius)
    local x, y, z = zed:getX(), zed:getY(), zed:getZ()
    local squares = {}

    for dx = -radius, radius do
        for dy = -radius, radius do
            local nx, ny = x + dx, y + dy
            local sq = getCell():getOrCreateGridSquare(nx, ny, z)
            if vTrait.isWalkable(sq) then
                table.insert(squares, sq)
            end
        end
    end

    if #squares > 0 then
        return squares[ZombRand(1, #squares + 1)]
    end

    return nil
end

function vTrait.isWithinRange(pl, zed, range)
	if not range then return end
	local dist
	if pl and zed then
		dist = pl:DistTo(zed:getX(), zed:getY())
	end
    return dist <= range
end

function vTrait.isClosestPl(pl, zed)
	if not pl then return end
	if not zed then return end
	if pl:HasTrait("V") then
		local plDist = vTrait.checkDist(pl, zed)
		local compare = round(zed:distToNearestCamCharacter())
		if plDist == compare then
			return true
		end
		return false
	end
end

function vTrait.getDistanceBetween(obj1, obj2)
    local dx = obj1:getX() - obj2:getX()
    local dy = obj1:getY() - obj2:getY()
    return math.sqrt(dx * dx + dy * dy)
end
